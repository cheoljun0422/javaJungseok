package org.example.ch02;

public class Ch2_4Constant {
    public static void main(String[] args) {
        final int value;
        value = 100;
        //value = 200; 상수는 변하지 않음
        System.out.println(value);

        /* 변수 : 하나의 값을 저장하는 공간
        상수 : 값을 한번만 저장할 수 있는 공간
        리터럴 : 값 자체
        */
    }
}
